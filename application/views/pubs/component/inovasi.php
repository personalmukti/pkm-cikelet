                            <h4><strong>Pencapaian Kegiatan</strong></h4>
                              <ol>
                                <li>K1
                                  <p>K1 adalah kunjungan pertama ibu setelah diketahui bahwa ia dalam keadaan hamil pada usia kehamilan kurang dari 16 minggu. Hasil kegiatan pelayanan antenatal care K1. Capaian K1 dari 8 desa hanya 1 desa yang belum mencapai target yaitu desa cikelet. Sedangkan 7 desa yang lain sudah mencapai target dan pencapaian Puskesmas yaitu 102,75.</p>
                                </li>

                                <li>K4
                                  <p>Pada kegiatan ini adalah cakupan ibu hamil pada trimester ketiga setelah melewati kunjungan kedua dan ketiga. Pada kunjungan ini peran Bidan adalah menentukan keadaan ibu dan janin baik  dengan pemeriksaan 10 T dan yang paling penting adalah perencanaan persalinan yang aman dan bersih. Pencapaian K4 suah mencapai trarget untuk tingkat puskesmas. Dari semua desa baru 5 desa yang sudah mencapai target dan 3 desa yang belum mencapai target dikarenakan sistem pencatatan dan penjaringan kunjungan yang kurang dilaksanakan oleh bidan. Ibu hamil yang tidak datang ke Posyandu atau tidak memeriksakan kehamilan sama sekali, harus dilaksanakan kunjungan rumah sebagai tindakan awal deteksi resiko dan masalah yang bisa saja terjadi pada proses persalinan.</p>
                                </li>

